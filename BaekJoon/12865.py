import sys ; input = sys.stdin.readline

def chk(K, wLi, vLi, N) : 
    dp = [[0]*(K+1) for _ in range(N+1)]
    for i in range(1, N+1) : 
        for j in range(1, K+1) : 
            if wLi[i-1] <= j : 
                dp[i][j] = max(vLi[i-1] + dp[i-1][j-wLi[i-1]], dp[i-1][j])
            else : 
                dp[i][j] = dp[i-1][j]
    return dp[N][K]

if __name__ == "__main__" : 
    N, K = map(int, input().split())
    wLi = [0] * N        # weight list
    vLi = [0] * N        # value list
    for i in range(N) : 
        W, V = map(int, input().split())
        wLi[i] = W
        vLi[i] = V
    
    print(chk(K, wLi, vLi, N))