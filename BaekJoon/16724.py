import sys
input = sys.stdin.readline


def move(y, x, counting) : 
    global NOS
    if visited[y][x] != -1 : 
        if visited[y][x] == counting : 
            NOS += 1
        return
    
    visited[y][x] = counting

    if land[y][x] == "L" : move(y, x-1, countingNum)
    if land[y][x] == "R" : move(y, x+1, countingNum)
    if land[y][x] == "U" : move(y-1, x, countingNum)
    if land[y][x] == "D" : move(y+1, x, countingNum)


if __name__ == "__main__" : 
    M, N = map(int, input().split())
    land = [[] for _ in range(M)]
    for i in range(M) : 
        land[i] = list(input().strip())

    visited = [[-1 for _ in range(N)] for _ in range(M)]
    countingNum = 0
    NOS = 0             # NOS = Number Of Safe zones

    for i in range(M) : 
        for j in range(N) : 
            move(i, j, countingNum)
            countingNum += 1

    print(NOS)